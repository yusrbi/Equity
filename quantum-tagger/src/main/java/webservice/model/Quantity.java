package webservice.model;

public class Quantity {
	
	private String unit;
	private String class_;
	private String measure;
	private String magnitude;
	private String unit_wiki_url;
	private String measure_wiki_url;
	
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public String getClass_() {
		return class_;
	}
	public void setClass_(String class_) {
		this.class_ = class_;
	}
	public String getMeasure() {
		return measure;
	}
	public void setMeasure(String measure) {
		this.measure = measure;
	}
	public String getMagnitude() {
		return magnitude;
	}
	public void setMagnitude(String magnitude) {
		this.magnitude = magnitude;
	}
	public String getUnit_wiki_url() {
		return unit_wiki_url;
	}
	public void setUnit_wiki_url(String unit_wiki_url) {
		this.unit_wiki_url = unit_wiki_url;
	}
	public String getMeasure_wiki_url() {
		return measure_wiki_url;
	}
	public void setMeasure_wiki_url(String measure_wiki_url) {
		this.measure_wiki_url = measure_wiki_url;
	}
	
	

}
